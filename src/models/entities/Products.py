
class Product():
    
    def __init__(self, id, name, description, price, quantity, category_id, created_at, updated_at):
        self.id = id
        self.name = name
        self.description = description
        self.price = price
        self.quantity = quantity
        self.category_id = category_id
        self.created_at = created_at
        self.updated_at = updated_at
